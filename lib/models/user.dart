  final String id;
  final String name;
  final String email;
  final String avatarUrl;
  final String lastName;
  final String address;
  final String zipCode;
  final String state;
  final String city;
  final String number;
  final String complement;

  const User({
    this.id,
    this.name,
    this.email,
    this.avatarUrl,
    this.lastName,
    this.address,
    this.zipCode,
    this.state,
    this.city,
    this.number,
    this.complement,
  });
}
